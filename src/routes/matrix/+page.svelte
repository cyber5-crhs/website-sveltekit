  <h2>getting a client</h2>
  <p>
    The first thing you need to is download a client. There are many clients
    compatible with the Matrix Protocol, for mobile, desktop (e.g. Linux,
    FreeBSD, MacOS, Windows), web, and even Nintendo 3DS.
  </p>
  <p>
    <a href="https://element.io">Element</a>
    is a widely-used cross-platform client. Do you use Discord?
    <a href="https://matrix.org/ecosystem/clients/cinny">Cinny</a>
    will feel like home. Other clients we recommend include
    <a href="https://schildi.chat">SchildiChat</a>
    and <a href="https://fluffychat.im/">FluffyChat (mobile)</a>.
  </p>
  <p>
    There exist a wide assortment of clients that you can pick from. You can
    find a list of clients on the <a href="https://matrix.org/ecosystem/clients"
      >matrix.org website</a
    >.
  </p>

  <h2>registering an account</h2>
  <p>
    Once you download your client, all that's left do is to register an account.
  </p>
  <p>
    Matrix is decentralized, meaning that there are multiple homeservers you can
    choose from to create your account. These homeservers exchange messages with
    each other, so you can talk to people on different homeservers.
  </p>
  <p>
    <a href="https://matrix.org">Matrix.org</a> is a decent homeserver, but
    there are others we recommend such as
    <a href="https://envs.net">envs.net</a>. You can find a list of popular
    homeservers on
    <a href="https://joinmatrix.org/servers/">joinmatrix.org</a>.
  </p>
  <p>
    Good OPSEC dictates that you shouldn't reveal private information in your
    username. Keep in mind that you can set a nickname in the Matrix rooms using
    <code>/roomnick</code>.
  </p>

